void uart_puts_noint(char *s)
{
	while(*s != 0) {
		while((USART1->SR&0X40)==0);
		USART1->DR = *s++;
	}
}

void uart_put_data_noint(uint8 *data, uint32 length)
{
	while(length>0) {
		while((USART1->SR&0X40)==0);
		USART1->DR = *data++;
		length--;
	}
}

